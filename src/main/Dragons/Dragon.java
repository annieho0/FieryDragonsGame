package main.Dragons;

public abstract class Dragon {
    int dragonX;
    int dragonY;
    int dragonWidth;
    int dragonHeight;

    public Dragon(int dragonX, int dragonY, int dragonWidth, int dragonHeight) {
        this.dragonX = dragonX;
        this.dragonY = dragonY;
        this.dragonWidth = dragonWidth;
        this.dragonHeight = dragonHeight;
    }

}







